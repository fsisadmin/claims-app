// Map for quick lookup by state code
export const STATE_MAP = Object.fromEntries(STATES.map(s => [s.code, s]))

// Get state name from code
export function getStateName(code) {
  if (!code) return null
  const upper = code.toUpperCase().trim()
  return STATE_MAP[upper]?.name || null
}

// Get full state object from code
export function getState(code) {
  if (!code) return null
  const upper = code.toUpperCase().trim()
  return STATE_MAP[upper] || null
}

// Validate state code
export function isValidStateCode(code) {
  if (!code) return false
  return code.toUpperCase().trim() in STATE_MAP
}

// Get states by region
export function getStatesByRegion(region) {
  return STATES.filter(s => s.region === region)
}

// Get all unique regions
export const REGIONS = ['Northeast', 'Southeast', 'Midwest', 'Southwest', 'West']

// State codes sorted alphabetically by name (for dropdowns)
export const STATES_BY_NAME = [...STATES].sort((a, b) => a.name.localeCompare(b.name))

// State codes only (for validation)
export const STATE_CODES = STATES.map(s => s.code)
